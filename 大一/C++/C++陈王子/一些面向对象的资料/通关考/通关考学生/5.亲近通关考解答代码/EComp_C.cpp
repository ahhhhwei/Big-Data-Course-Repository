#include<iostream>
using namespace std;
//-------------------------------------
int a[34][34];
//-------------------------------------
int main(){
  for(int i=0; i<34; i++){
    a[i][0]=1;  // 每行第1个元素初始化
    for(int j=1; j<=i; j++)
      a[i][j] = a[i-1][j-1]+a[i-1][j];
  }
  for(int n,m; cin>>n>>m; )
    cout<<a[n][m]<<"\n";
}//------------------------------------
 